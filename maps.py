def parseMap(map):
    arrMap = map.split("\n")[1:]

    for i, j in enumerate(arrMap):
        arrMap[i] = [*arrMap[i]]

    for i, ii in enumerate(arrMap):
        for j, jj in enumerate(arrMap[i]):
            arrMap[i][j] = translations[arrMap[i][j]]

    return arrMap


